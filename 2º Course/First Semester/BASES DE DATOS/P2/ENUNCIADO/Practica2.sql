SELECT SUM(HORAS_TEORIA) "TOTALHORASTEORIA", SUM(HORAS_PRACTICA) "TOTALHORASPRACTICAS"
FROM ASIGNATURAS A, DOCENCIA D
WHERE A.SIGLAS= D.SIGLAS;

//9

SELECT SUM(HORAS_TEORIA) "TOTALHORASTEORIA", SUM(HORAS_PRACTICA) "TOTALHORASPRACTICAS"
FROM ASIGNATURAS A, DOCENCIA D, PROFESORES P
WHERE A.SIGLAS= D.SIGLAS  AND D.PROFESOR = P.CODIGO AND A.ANUALIDAD = 'A' AND P.CODIGO > 30;

//10

SELECT COUNT(*) "NUMASIG" ,AVG(HORAS_PRACTICA) "MEDIAHORASPRACTICA"
FROM ASIGNATURAS A, PROFESORES P, DOCENCIA D
WHERE A.ANUALIDAD = 'A' AND P.CODIGO >30 AND A.SIGLAS = D.SIGLAS AND D.PROFESOR = P.CODIGO;

