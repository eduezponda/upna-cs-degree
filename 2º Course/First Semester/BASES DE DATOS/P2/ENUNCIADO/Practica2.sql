//12

SELECT MAX(CAPACIDAD) "MAXCAPACIDAD"
FROM LOCALES L, ASIGNATURAS A, DOCENCIA D
WHERE A.HORAS_PRACTICA != 0 AND L.CODIGO = D.LOCAL AND A.SIGLAS = D.SIGLAS;

//13

SELECT COUNT(*) "LOCALES1"
FROM LOCALES L
WHERE L.SITUACION LIKE 'Planta Primera%';

//14

SELECT DISTINCT L.SITUACION "SITUACION", COUNT(L.SITUACION) "NUMLOCAL"
FROM LOCALES L
GROUP BY L.SITUACION // el group by es para cada situacion decir el numero de locales
ORDER BY L.SITUACION;

//15

SELECT D.DIA "D", MIN(D.HORA) "PRIMERA_HORA",MAX(D.HORA) "ULTIMA_HORA"
FROM DOCENCIA D
GROUP BY D.DIA
ORDER BY D.DIA;

//16

SELECT ANUALIDAD "A",COUNT(SIGLAS) "NUM_ASIGNATURAS" ,AVG(CREDITOS) "MEDIA_CREDITOS",MIN(ALUMNOS) "MINIMO_ALUMNOS"
FROM ASIGNATURAS
WHERE ANUALIDAD != 'A' OR ANUALIDAD IS NULL
GROUP BY ANUALIDAD
ORDER BY ANUALIDAD;

//17

SELECT  A.SIGLAS "SIGLAS", A.NOMBRE "NOMBRE"
FROM ASIGNATURAS A, DOCENCIA D
WHERE A.SIGLAS = D.SIGLAS 
GROUP BY A.SIGLAS,A.NOMBRE
HAVING COUNT(*) > 9 AND COUNT(*) < 21
ORDER BY A.NOMBRE;

//18

SELECT D.HORA "HORA", COUNT(*) "NUM_DIAS"
FROM DOCENCIA D
WHERE D.SIGLAS = 'Alg'
GROUP BY D.HORA
HAVING COUNT(*) > 1 
ORDER BY COUNT(*) DESC; //ORDER BY NUM_DIAS DESC