SELECT (A.NOMBRE || '('|| A.SIGLAS|| ')') AS ASIGNATURA , A.ALUMNOS /COUNT (*)"ALUMNOS_POR_CLASE"
FROM ASIGNATURAS A,DOCENCIA D
WHERE A.SIGLAS=D.SIGLAS
GROUP BY A.NOMBRE, A.SIGLAS,A.ALUMNOS
ORDER BY "ALUMNOS_POR_CLASE" DESC;

