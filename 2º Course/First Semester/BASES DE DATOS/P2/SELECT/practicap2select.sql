SELECT G.NOMBRE "GRUPO"
FROM GRUPOS G, DOCENCIA D
WHERE D.CURSO=G.CURSO AND D.CLASE=G.CLASE AND D.GRUPO=G.CODIGO
HAVING COUNT(DISTINCT D.PROFESOR) = 1
GROUP BY G.NOMBRE;

