SELECT L.NOMBRE "NOMBRE",L.EDIFICIO "EDIFICIO", MAX(D.CURSO) "CURSO_MAS_ALTO"
FROM LOCALES L, DOCENCIA D
WHERE L.CODIGO = D.LOCAL
GROUP BY L.NOMBRE, L.EDIFICIO
ORDER BY L.NOMBRE;

