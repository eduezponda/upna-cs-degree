SELECT D.CURSO "CURSO",MAX (A.ALUMNOS) "MAYOR_NUM_ALUMNOS",MIN(A.ALUMNOS) "MENOR_NUM_ALUMNOS",AVG(A.ALUMNOS) "MEDIA_NUM_ALUMNOS"
FROM DOCENCIA D, ASIGNATURAS A
WHERE A.SIGLAS = D.SIGLAS
GROUP BY D.CURSO;

