SELECT (P.APELLIDO1 || ' ' || P.APELLIDO2 || ', '|| P.NOMBRE_PILA ) "NOMBRE_Y_APELLIDOS"
FROM PROFESORES P, DOCENCIA D
WHERE P.CODIGO = D.PROFESOR
GROUP BY P.APELLIDO1, P.APELLIDO2, P.NOMBRE_PILA
HAVING COUNT(DISTINCT D.SIGLAS) > 1
