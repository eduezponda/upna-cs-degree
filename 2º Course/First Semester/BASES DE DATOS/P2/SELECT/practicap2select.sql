SELECT G.NOMBRE AS GRUPO, COUNT (DISTINCT D.PROFESOR) "DISTINTOS_PROFESORES"
FROM DOCENCIA D, GRUPOS G
WHERE D.CURSO=G.CURSO AND D.CLASE=G.CLASE AND D.GRUPO=G.CODIGO
GROUP BY G.NOMBRE;

