SELECT A.NOMBRE "NOMBRE", D.DIA "DIA", COUNT (DISTINCT P.CODIGO) "PROFESORES_NO_TIEMPO_COMPLETO"
FROM ASIGNATURAS A, DOCENCIA D, PROFESORES P
WHERE A.SIGLAS = D.SIGLAS AND D.PROFESOR = P.CODIGO AND P.DEDICACION != 'TC' 
GROUP BY A.NOMBRE, D.DIA
HAVING COUNT (DISTINCT P.CODIGO) > 1;

//22

SELECT A.CURSO "CURSO",(A.NOMBRE || '('|| A.SIGLAS|| ')') AS ASIGNATURA , A.GRUPOS_TEORIA "TOTAL_GRUPOS_TEORIA", A.GRUPOS_PRACTICA "TOTAL_GRUPOS_PRACTICA"
FROM ASIGNATURAS A
WHERE A.CREDITOS > 9 AND A.GRUPOS_TEORIA > 0 AND A.GRUPOS_PRACTICA > 0
ORDER BY A.CURSO, A.NOMBRE;

